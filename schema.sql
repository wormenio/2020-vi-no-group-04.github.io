
    create table Compras (
        id integer generated by default as identity (start with 1),
        entidad_id integer not null,
        fecha varbinary(255),
        medio_de_pago_id integer not null,
        medio_de_pago_nro integer not null,
        monto_total double,
        proveedor_id integer,
        primary key (id)
    )

    create table Items (
        id integer generated by default as identity (start with 1),
        nombre varchar(255),
        primary key (id)
    )

    create table Proveedores (
        id integer generated by default as identity (start with 1),
        apellido varchar(255),
        codigo_postal varchar(255),
        cuit varchar(255),
        dni varchar(255),
        nombre varchar(255),
        razon_social varchar(255),
        primary key (id)
    )

    create table Tipos_Documentos_comerciales (
        id integer generated by default as identity (start with 1),
        nombre varchar(255),
        primary key (id)
    )

    create table documentos_comerciales (
        id integer generated by default as identity (start with 1),
        numero integer not null,
        tipo_documento_comercial_id integer,
        compra_id integer,
        primary key (id)
    )

    create table items_de_compra (
        compra_id bigint not null,
        item_id bigint not null,
        cantidad integer not null,
        monto double,
        primary key (compra_id, item_id)
    )

